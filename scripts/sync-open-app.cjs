/**
 * Regenerates repo-root open-app.html from frontend/index.html + Vite dev port
 * so you don’t have to hand-edit the launcher when copy or port changes.
 *
 * Usage: node scripts/sync-open-app.cjs
 * Runs at the start of `npm run dev` (scripts/dev.cjs) and via `npm run sync:open-app`.
 */

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const indexPath = path.join(root, 'frontend/index.html');
const vitePath = path.join(root, 'frontend/vite.config.ts');
const outPath = path.join(root, 'open-app.html');

function readMeta(html, name) {
  const re = new RegExp(`name="${name}"[\\s\\S]*?content="([^"]*)"`, 'i');
  const m = html.match(re);
  return m ? m[1].trim() : '';
}

function readOg(html, prop) {
  const re = new RegExp(`property="${prop}"[\\s\\S]*?content="([^"]*)"`, 'i');
  const m = html.match(re);
  return m ? m[1].trim() : '';
}

function readTitle(html) {
  const m = html.match(/<title>([^<]*)<\/title>/i);
  return m ? m[1].trim() : 'The Pigsty';
}

function resolveWebPort() {
  if (process.env.VITE_DEV_PORT) return String(parseInt(process.env.VITE_DEV_PORT, 10) || 5173);
  if (process.env.WEB_PORT) return String(parseInt(process.env.WEB_PORT, 10) || 5173);
  try {
    const vc = fs.readFileSync(vitePath, 'utf8');
    const m = vc.match(/\bport:\s*(\d+)/);
    if (m) return m[1];
  } catch {
    /* ignore */
  }
  return '5173';
}

const indexHtml = fs.readFileSync(indexPath, 'utf8');
const title = readTitle(indexHtml);
const description = readMeta(indexHtml, 'description') || readOg(indexHtml, 'og:description');
const ogTitle = readOg(indexHtml, 'og:title') || title;
const ogDescription = readOg(indexHtml, 'og:description') || description;
const webPort = resolveWebPort();
/** Match Vite `server.host: '127.0.0.1'` so the launcher hits the same socket the dev server opens. */
const appOrigin = `http://127.0.0.1:${webPort}`;

